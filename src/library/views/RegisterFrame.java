    private void registerActionPerformed(java.awt.event.ActionEvent evt) {
        // Lấy thông tin từ các trường nhập liệu
        String name = nameTextField.getText(); // Giả sử bạn có TextField với tên nameTextField
        String email = emailTextField.getText(); // Giả sử bạn có TextField với tên emailTextField
        String username = usernameTextField.getText(); // Giả sử bạn có TextField với tên usernameTextField
        String password = new String(passwordField.getText()); // Giả sử bạn có PasswordField với tên passwordField
        String phone = phoneTextField.getText(); // Giả sử bạn có TextField với tên phoneTextField
        String address = addressTextField.getText(); // Giả sử bạn có TextField với tên addressTextField

        // Kiểm tra nếu có trường nào rỗng
        if (name.trim().isEmpty() || email.trim().isEmpty() || username.trim().isEmpty() || password.trim().isEmpty() || phone.trim().isEmpty() || address.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng điền đầy đủ thông tin!", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            return; // Dừng lại nếu có trường rỗng
        }

        // Kiểm tra tính hợp lệ của email (Ví dụ: kiểm tra email có đúng định dạng không)
        if (!email.matches("^[a-zA-Z0-9_+&*-]+(?:\\.[a-zA-Z0-9_+&*-]+)*@(?:[a-zA-Z0-9-]+\\.)+[a-zA-Z]{2,7}$")) {
            JOptionPane.showMessageDialog(this, "Email không hợp lệ!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Tạo đối tượng MemberDAO để lưu thông tin thành viên vào cơ sở dữ liệu
        MemberDAO memberDAO = new MemberDAO();
        boolean isRegistered = memberDAO.registerMember(name, email, username, password, phone, address);

        // Kiểm tra kết quả đăng ký
        if (isRegistered) {
            JOptionPane.showMessageDialog(this, "Đăng ký thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            UserLoginFrame userLoginFrame = new UserLoginFrame();
            userLoginFrame.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Tên đăng nhập đã tồn tại hoặc đăng ký thất bại, vui lòng thử lại!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }



